    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        jLabel6.setText("");
        try {
            if (currentAccount.equals("TFSA")) {
                double temp = currentAcc.balance;
                tfsaAcc.deposit(Double.parseDouble(jTextField1.getText()));
                if (temp == currentAcc.balance) {
                    jLabel6.setText("Contribution Limit Reached (Max 6000 per year)");
                }
            } else {
                currentAcc.deposit(Double.parseDouble(jTextField1.getText()));
            }
        } catch (NumberFormatException e) {
            jLabel6.setText("Please enter a number value");
        
        }
        
        updateBalance();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        jLabel7.setText("");
        try {
            double temp = currentAcc.balance;
            if (currentAccount.equals("Savings")) {
                savingsAcc.withdraw(Double.parseDouble(jTextField2.getText()));
                jLabel7.setText("$5 withdrawal fee applied");
            } else {
                currentAcc.withdraw(Double.parseDouble(jTextField2.getText()));
            }
            if (temp == currentAcc.balance) {
                jLabel7.setText("Insufficient funds");
            }
        } catch (NumberFormatException e) {
            jLabel7.setText("Please enter a number value");
        }
        
        updateBalance();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        for (int i = 0; i < 12; i++) {
            savingsAcc.addInterest();
            chequingAcc.addInterest();
            tfsaAcc.addInterest();
        }
        tfsaAcc.newYear();
        updateBalance();
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        savingsAcc.addInterest();
        chequingAcc.addInterest();
        tfsaAcc.addInterest();
        month++;
        if (month % 12 == 0) {
            tfsaAcc.newYear();
        }
        updateBalance();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String fromAccount = jComboBox2.getSelectedItem().toString();
        String toAccount = jComboBox3.getSelectedItem().toString();
        
        if (!fromAccount.equals(toAccount)) {
            Debit fromAcc = new Debit(0,0);
            Debit toAcc = new Debit(0,0);
            if (fromAccount.equals("Savings")) {
                fromAcc = savingsAcc;
            } else if (fromAccount.equals("Chequing")) {
                fromAcc = chequingAcc;
            } else if (fromAccount.equals("TFSA")) {
                fromAcc = tfsaAcc;
            }
            if (toAccount.equals("Savings")) {
                toAcc = savingsAcc;
            } else if (toAccount.equals("Chequing")) {
                toAcc = chequingAcc;
            } else if (toAccount.equals("TFSA")) {
                toAcc = tfsaAcc;
            }
            
            double temp1 = fromAcc.balance;
            double temp2 = toAcc.balance;
            
            fromAcc.withdraw(Double.parseDouble(jTextField4.getText()));
            
            if (temp1 != fromAcc.balance) {
                toAcc.deposit(Double.parseDouble(jTextField4.getText()));
                if (temp2 != toAcc.balance) {
                    jLabel12.setText("Transfer Success");
                } else {
                    jLabel12.setText("Error transferring funds");
                    fromAcc.deposit(Double.parseDouble(jTextField4.getText()));
                }
            } else {
                jLabel12.setText("Insufficient funds");
            }
        } else {
            jLabel12.setText("Cannot transfer to same account");
        }
        
        updateBalance();
    }//GEN-LAST:event_jButton5ActionPerformed
    
