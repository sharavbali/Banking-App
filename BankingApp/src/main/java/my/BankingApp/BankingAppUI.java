    public void updateBalance() {
        if (currentAccount.equals("Savings")) {
            currentAcc = savingsAcc;
        } else if (currentAccount.equals("Chequing")) {
            currentAcc = chequingAcc;
        } else if (currentAccount.equals("TFSA")) {
            currentAcc = tfsaAcc;
        }
        jLabel3.setText(currentAccount + " Account");
        jLabel2.setText("$"+String.valueOf(String.format("%,.2f", currentAcc.balance)));
    }
    
